package com.upskill.java_1;

public class AccessModifier {

// public - Any class an access
// private - only same class can access
// protected - classes in the same class package and subclass
// default - classes in same package can access
	
public String name = "Lucky";  // public - Any class an access

private int age = 30; // private - only same class can access

protected int ssn = 123456789; // protected - classes in the same class package and subclass

String address = "East Elmhurst";





}
